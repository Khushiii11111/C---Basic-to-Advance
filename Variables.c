#include <stdio.h>

int main() {

//We are learning variables. (Single Line Comment)

    int number = 2;
    printf("number is %d\n", number);

    char star = '*';
    printf("star looks like %c\n", star);

    float pi = 3.14;
    printf("value of pi is %f\n", pi);

    int a = 50;
    printf("a is %d\n", a);

    int A = 70;
    printf("A is %d\n", A);

    int age = 11;
    printf("age is %d\n", age);

    int final_price = 150;
    printf("price is %d\n", final_price);
    return 0;
}

